    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        int yes = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Ingin Menghapus Data Ini?!","Konfirmasi",JOptionPane.YES_NO_OPTION);
        if (yes==0){
            String sql = "delete from menu where id_menu='"+txt_idmenu.getText()+"'";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus!");
                kosong();
                txt_idmenu.requestFocus();
            } catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data Gagal Dihapus!"+e);
            } datatabel();
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        try{
            String sql = "update menu set nama_menu=?,kategori=?,harga_jual=?,stok_menu=? "
            + "where id_menu='"+txt_idmenu.getText()+"'";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txt_namamenu.getText());
            stat.setString(2, cb_kategorimenu.getSelectedItem().toString());
            stat.setString(3, txt_hargajualmenu.getText());
            stat.setString(4, txt_stokmenu.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah!");
            kosong();
            txt_idmenu.requestFocus();
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah!"+e);
        } datatabel();
    }//GEN-LAST:event_btn_ubahActionPerformed

