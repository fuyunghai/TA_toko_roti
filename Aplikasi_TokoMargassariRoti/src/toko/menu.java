    protected void datatabel(){
        Object[] Baris = {"ID Menu","Nama Menu","Kategori","Harga Jual","Stok Barang"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txt_carimenu.getText();
        try{
            String sql = "SELECT * FROM menu where kategori like'%"+cariitem+"%' or "
                    + "nama_menu like'%"+cariitem+"%'order by id_menu asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5)
                });
            }
            tbl_menu.setModel(tabmode);
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Data Tidak Dapat Dimuat!"+e);
        }
        
    }
    
    protected void autonumber() {
        try {
            String sql = "SELECT id_menu FROM menu ORDER BY id_menu ASC";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            txt_idmenu.setText("MN0001");
            while (rs.next()) {
                String id_menu = rs.getString("id_menu");
                String numericPart = id_menu.substring(2); 
                int AN = Integer.parseInt(numericPart) + 1;
                String Nol = "";
                if (AN < 10) {
                    Nol = "000";
                } else if (AN < 100) {
                    Nol = "00";
                } else if (AN < 1000) {
                    Nol = "0";
                } else if (AN < 10000) {
                    Nol = "";
                }
                txt_idmenu.setText("MN" + Nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto Number is Failed" + e);
        }
    }

